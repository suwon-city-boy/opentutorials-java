package org.opentutorials.java.basics;

public class Character {

	public static void main(java.lang.String[] args) {
		System.out.println('생'); //문자
		System.out.println("생활코딩"); //문자열
		System.out.println("생"); //문자열(문자열에 작은 따옴표는 불가)
		
		System.out.println("Kim said \"Hello\""); //이스케이프
		System.out.println("Kim said \'H\'"); //이스케이프
		
		System.out.println("\nHi!\nMy name is Kim!\n");//줄바꿈
		
		System.out.println("Hi!" + " My name is Kim!");//문자열의 연산
	}

}
